var app = angular.module('app', ['ngRoute','ngSanitize']);

app.config(function($routeProvider, $locationProvider){
	$routeProvider
		.when('/',
		{
			controller:'dashboard',
			templateUrl:'views/home_loggedin.html'
		})
		.when('/market',
		{
			controller:'market',
			templateUrl:'views/market.html'
		})
		/*
		.when('/users/:user_id', 
                {   
                    controller:UserView, 
                    templateUrl: function(params){ return '/users/view/' + params.user_id; }
                }
            )
		*/
		.when('/user/:id',
		{
			controller:'userView',
			templateUrl:'views/user.html'
		})
		.when('/:username',
		{
			controller:'usernameView',
			templateUrl:'views/user.html'
		})
		.when('/profile',
		{
			controller:'profileView',
			templateUrl:'views/profile.html'
		})
		.when('/creatives',
		{
			controller:'creativesView',
			templateUrl:'views/creatives.html'
		})
		.when('/profile',
		{
			controller:'profileView',
			templateUrl:'views/profile.html'
		})
		.when('/project/',
		{
			controller:'projectView',
			templateUrl:'views/project.html'
		})
		.when('/projects',
		{
			controller:'projectsView',
			templateUrl:'views/projects.html'
		})
		.when('/projects/new',
		{
			controller:'projectsView',
			templateUrl:'views/projects_new.html'
		})
		.when('/offerings/view/:id',
		{
			controller:'offeringsView',
			templateUrl:'views/offerings_view.html'
		})
		.when('/offerings/new',
		{
			controller:'offeringsEdit',
			templateUrl:'views/offerings/offerings_edit.html'
		})
		.when('/offerings/order/:id',
		{
			controller:'about',
			templateUrl:'views/help/about.html'
		})
		.when('/offerings/edit/:id',
		{
			controller:'offeringsEdit',
			templateUrl:'views/offerings_edit.html'
		})
		.when('/offerings/confirm:id',
		{
			controller:'offeringsConfirm',
			templateUrl:'views/offerings_confirm.html'
		})
		.when('/offerings/delete/:id',
		{
			controller:'offeringsDelete',
			templateUrl:'views/offerings_delete.html'
		})
		.when('/external',
		{
			controller:'external',
			templateUrl:'views/external.html'
		})
		.when('/work',
		{
			controller:'workView',
			templateUrl:'views/work.html'
		})
		.when('/work/invoices',
		{
			controller:'workInvoicesView',
			templateUrl:'views/work_invoices.html'
		})
		.when('/work/invoices/new',
		{
			controller:'workInvoicesNew',
			templateUrl:'views/work.html'
		})
		.when('/work/invoices/edit/:id',
		{
			controller:'workView',
			templateUrl:'views/work_invoices_edit.html'
		})
		.when('/admin',
		{
			controller:'admin',
			templateUrl:'views/admin.html'
		})
		.when('/admin/users',
		{
			controller:'adminUsers',
			templateUrl:'views/admin_users.html'
		})
		.when('/error',
		{
			controller:'error',
			templateUrl:'views/error.html'
		})
		.otherwise({ redirectTo: '/error' });
		// use the HTML5 History API
		$locationProvider.html5Mode(true);
});